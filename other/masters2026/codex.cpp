struct XorShift64 {
    uint64_t x;

    explicit XorShift64(uint64_t seed) : x(seed ? seed : 88172645463393265ULL) {}

    uint64_t next_u64() {
        x ^= x << 7;
        x ^= x >> 9;
        return x;
    }

    int next_int(int n) {
        return static_cast<int>(next_u64() % static_cast<uint64_t>(n));
    }

    double next_double() {
        return (next_u64() >> 11) * (1.0 / 9007199254740992.0);
    }
};

static inline void set_bit(Mask &m, int cell) {
    m.w[cell >> 6] |= (1ULL << (cell & 63));
}

static inline void clear_bit(Mask &m, int cell) {
    m.w[cell >> 6] &= ~(1ULL << (cell & 63));
}

static inline bool test_bit(const Mask &m, int cell) {
    return ((m.w[cell >> 6] >> (cell & 63)) & 1ULL) != 0ULL;
}

static inline bool is_zero(const Mask &m) {
    for (uint64_t x : m.w) {
        if (x) return false;
    }
    return true;
}

static inline int popcount(const Mask &m) {
    int s = 0;
    for (uint64_t x : m.w)
        s += __builtin_popcountll(x);
    return s;
}

static inline int popcount_and(const Mask &a, const Mask &b) {
    int s = 0;
    for (int wi = 0; wi < WORDS; wi++) {
        s += __builtin_popcountll(a.w[wi] & b.w[wi]);
    }
    return s;
}

template <class F>
static inline void for_each_cell(const Mask &m, F &&f) {
    for (int wi = 0; wi < WORDS; wi++) {
        uint64_t x = m.w[wi];
        while (x) {
            int b = __builtin_ctzll(x);
            int cell = (wi << 6) + b;
            if (cell < CELLS) f(cell);
            x &= (x - 1);
        }
    }
}

